-- ---------- ЗАХИАЛГА ХИЙХ ФУНКЦ ----------
-- Захиалгыг бүртгэж, тухайн барааны нөөцийг нэг гүйлгээгээр хорогдуулна.

create or replace function public.place_order(
  p_id text, p_items jsonb, p_total bigint,
  p_customer text, p_phone text, p_address text, p_city text, p_pay text
) returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  itm jsonb;
begin
  insert into public.orders (id, date, items, total, customer, phone, address, city, pay, status)
  values (
    p_id,
    to_char(now() at time zone 'Asia/Ulaanbaatar', 'YYYY-MM-DD'),
    coalesce(p_items, '[]'::jsonb),
    coalesce(p_total, 0),
    coalesce(p_customer, ''), coalesce(p_phone, ''),
    coalesce(p_address, ''), coalesce(p_city, ''), coalesce(p_pay, ''),
    'Хүлээгдэж буй'
  );

  for itm in select * from jsonb_array_elements(coalesce(p_items, '[]'::jsonb))
  loop
    if (itm ? 'product_id') then
      update public.products
      set stock = greatest(0, stock - coalesce((itm->>'qty')::int, 0))
      where id = (itm->>'product_id')::bigint;
    end if;
  end loop;
end;
$$;

revoke all on function public.place_order(text, jsonb, bigint, text, text, text, text, text) from public;
grant execute on function public.place_order(text, jsonb, bigint, text, text, text, text, text) to anon, authenticated;

